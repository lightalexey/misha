k = 0
k2 = 0
k3 = 0
k5 = 0
for i in range(100, 999 + 1):
    k += 1  
    if i % 2 == 0:
        k2 += 1
    if i % 3 == 0 :
            k3 += 1
    if i % 5 == 0:
                k5 += 1

print('Количество трехзначных чисел равно', k)
print('Количество трехзначных четных чисел равно', k2)
print('Количество трехзначных чисел кратных 3 равно', k3)
print('Количество трехзначных чисел кратных 5 равно', k5)