k = 0
k2 = 0
k3 = 0
k5 = 0
# k = k2 = 0
# k, k2 = 0, 0
for i in range(10, 99 + 1):
    # print(i)
    k += 1  # k = k + 1   k++
    if i % 2 == 0:
        k2 += 1
    if i % 3 == 0 :
            k3 += 1
    if i % 5 == 0:
                k5 += 1

print('Количество двухзначных чисел равно', k)
print('Количество двухзначных четных чисел равно', k2)
print('Количество двухзначных чисел кратных 3 равно', k3)
print('Количество двухзначных чисел кратных 5 равно', k5)